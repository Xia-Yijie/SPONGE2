def _apply_random_perturbation(
    base_coords: np.ndarray, box: np.ndarray, perturbation: float, seed: int
):
    coords = np.array(base_coords, dtype=float, copy=True)
    if perturbation <= 0:
        return coords
    rng = np.random.RandomState(seed)
    noise = (rng.random_sample(coords.shape) - 0.5) * 2.0 * perturbation
    coords += noise
    coords %= box
    return coords


def _write_lammps_bond_data(path: Path, coords, box, bonds):
    num_atoms = coords.shape[0]
    num_bonds = len(bonds)
    atom_lines = [
        f"{atom_id} 1 1 {coords[atom_id - 1, 0]:.12f} {coords[atom_id - 1, 1]:.12f} {coords[atom_id - 1, 2]:.12f}"
        for atom_id in range(1, num_atoms + 1)
    ]
    bond_lines = [
        f"{bond_id} 1 {atom_i} {atom_j}"
        for bond_id, (atom_i, atom_j) in enumerate(bonds, start=1)
    ]
    path.write_text(
        "\n".join(
            [
                "Generated by SPONGE custom_force test",
                "",
                f"{num_atoms} atoms",
                f"{num_bonds} bonds",
                "1 atom types",
                "1 bond types",
                "",
                f"0.0 {box[0]:.12f} xlo xhi",
                f"0.0 {box[1]:.12f} ylo yhi",
                f"0.0 {box[2]:.12f} zlo zhi",
                "",
                "Masses",
                "",
                "1 12.0",
                "",
                "Atoms",
                "",
                *atom_lines,
                "",
                "Bonds",
                "",
                *bond_lines,
                "",
            ]
        )
    )


def _run_lammps(lammps_dir: Path, check: bool = True):
    result = subprocess.run(
        ["lmp", "-in", "in.lammps", "-log", "log.lammps"],
        cwd=lammps_dir,
        capture_output=True,
        text=True,
        check=False,
    )
    output = result.stdout + "\n" + result.stderr
    if check and result.returncode != 0:
        raise RuntimeError(
            "LAMMPS failed. "
            f"cwd={lammps_dir}, code={result.returncode}\n"
            f"Output tail:\n{output[-4000:]}"
        )
    return result


def _detect_lammps_version():
    result = subprocess.run(
        ["lmp", "-h"],
        capture_output=True,
        text=True,
        check=False,
    )
    output = result.stdout + "\n" + result.stderr
    if result.returncode != 0:
        raise RuntimeError(
            f"Failed to query LAMMPS version.\nOutput tail:\n{output[-3000:]}"
        )
    for line in output.splitlines():
        if line.strip():
            return line.strip()
    return "unknown"


def _prepare_case_dir(outputs_path: Path, case_group: str, iteration: int):
    case_dir = outputs_path / case_group / str(iteration)
    if case_dir.exists():
        shutil.rmtree(case_dir)
    case_dir.mkdir(parents=True, exist_ok=True)
    return case_dir


def _save_array(
    reference_root: Path, rel_root: str, case_name: str, iteration: int, arr
):
    rel = Path(rel_root) / case_name / f"iter{iteration}.npy"
    path = reference_root / rel
    path.parent.mkdir(parents=True, exist_ok=True)
    np.save(path, np.asarray(arr, dtype=np.float64))
    return str(rel).replace("\\", "/")


def _extract_reaxff_dimer_thermo(log_path: Path):
    thermo = {}
    lines = log_path.read_text().splitlines()
    for i, line in enumerate(lines):
        if "Step" in line and "v_eb" in line and i + 1 < len(lines):
            headers = line.split()
            data = lines[i + 1].split()
            for header, value in zip(headers, data):
                thermo[header] = float(value)
            break
    if not thermo:
        raise ValueError(f"Failed to parse ReaxFF dimer thermo from {log_path}")
    return thermo


def _extract_lammps_charges(dump_path: Path):
    charges = {}
    lines = dump_path.read_text().splitlines()
    start = False
    for line in lines:
        if "ITEM: ATOMS" in line:
            start = True
            continue
        if not start:
            continue
        parts = line.split()
        if len(parts) < 2:
            continue
        atom_id = int(parts[0])
        charges[atom_id] = float(parts[1])
    if not charges:
        raise ValueError(f"Failed to parse charges from {dump_path}")
    return np.asarray([charges[i] for i in sorted(charges)], dtype=np.float64)


def _make_entry_common(
    case_name, iteration, perturbation, seed, natom, energy, pressure, stress
):
    return {
        "case_name": case_name,
        "iteration": int(iteration),
        "perturbation": float(perturbation),
        "seed": int(seed),
        "natom": int(natom),
        "energy": float(energy),
        "pressure": float(pressure),
        "stress": {k: float(stress[k]) for k in STRESS_KEYS},
    }


def _gen_cu_eam(
    iteration, statics_path, outputs_path, reference_root, lammps_utils
):
    curr_perturbation = 0.1 * iteration
    case_dir = _prepare_case_dir(outputs_path, "eam", iteration)
    lammps_dir = case_dir / "lammps"

    shutil.copytree(
        statics_path / "eam" / "lammps", lammps_dir, dirs_exist_ok=True
    )

    atoms = bulk("Cu", "fcc", a=3.615, cubic=True)
    atoms *= (14, 14, 14)

    seed = 42 + iteration * 100
    np.random.seed(seed)
    if iteration > 0:
        atoms.positions += (
            np.random.rand(*atoms.positions.shape) - 0.5
        ) * curr_perturbation

    coords = atoms.positions
    box = atoms.get_cell().diagonal()
    natom = len(atoms)

    lammps_utils.write_lammps_data(
        lammps_dir / "data.lammps", coords, box, masses=[63.546]
    )
    _run_lammps(lammps_dir)

    energy = lammps_utils.extract_lammps_potential(lammps_dir / "log.lammps")
    pressure = lammps_utils.extract_lammps_pressure(lammps_dir / "log.lammps")
    stress = lammps_utils.extract_lammps_stress(lammps_dir / "log.lammps")
    forces = (
        lammps_utils.extract_lammps_forces(lammps_dir)
        * lammps_utils.EV_TO_KCAL_MOL
    )

    entry = _make_entry_common(
        case_name="cu_eam",
        iteration=iteration,
        perturbation=curr_perturbation,
        seed=seed,
        natom=natom,
        energy=energy,
        pressure=pressure,
        stress=stress,
    )
    entry["forces_file"] = _save_array(
        reference_root, "forces", "cu_eam", iteration, forces
    )
    return entry



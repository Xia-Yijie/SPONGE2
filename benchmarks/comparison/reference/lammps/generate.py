def _entries_to_map(payload):
    result = {}
    for entry in payload.get("entries", []):
        key = (str(entry["case_name"]), int(entry["iteration"]))
        result[key] = entry
    return result


def compare_payloads(
    current_payload,
    generated_payload,
    current_root: Path,
    generated_root: Path,
    abs_tol_scalar: float,
    abs_tol_force: float,
    abs_tol_charge: float,
):
    for key in [
        "format_version",
        "energy_unit",
        "force_unit",
        "pressure_unit",
        "lammps_version",
    ]:
        if current_payload.get(key) != generated_payload.get(key):
            return (
                False,
                f"Metadata differs at '{key}': current={current_payload.get(key)!r}, "
                f"generated={generated_payload.get(key)!r}",
            )

    current_map = _entries_to_map(current_payload)
    generated_map = _entries_to_map(generated_payload)
    if set(current_map) != set(generated_map):
        current_only = sorted(set(current_map) - set(generated_map))
        generated_only = sorted(set(generated_map) - set(current_map))
        return (
            False,
            "Entry keys differ. "
            f"current-only={len(current_only)}, generated-only={len(generated_only)}",
        )

    max_scalar_diff = 0.0
    max_scalar_key = None
    max_scalar_name = None
    max_force_diff = 0.0
    max_force_key = None
    max_charge_diff = 0.0
    max_charge_key = None

    for key in sorted(current_map):
        current_entry = current_map[key]
        generated_entry = generated_map[key]

        for field in ["perturbation", "seed", "natom"]:
            curr_val = float(current_entry[field])
            gen_val = float(generated_entry[field])
            if abs(curr_val - gen_val) > 1.0e-12:
                return (
                    False,
                    f"{field} differs for {key}: current={curr_val}, generated={gen_val}",
                )

        for field in ["energy", "pressure"]:
            if field in current_entry or field in generated_entry:
                if field not in current_entry or field not in generated_entry:
                    return (
                        False,
                        f"{field} presence differs for {key}",
                    )
                diff = abs(
                    float(current_entry[field]) - float(generated_entry[field])
                )
                if diff > max_scalar_diff:
                    max_scalar_diff = diff
                    max_scalar_key = key
                    max_scalar_name = field

        current_stress = current_entry.get("stress")
        generated_stress = generated_entry.get("stress")
        if (current_stress is None) != (generated_stress is None):
            return False, f"stress presence differs for {key}"
        if current_stress is not None:
            for skey in STRESS_KEYS:
                diff = abs(
                    float(current_stress[skey]) - float(generated_stress[skey])
                )
                if diff > max_scalar_diff:
                    max_scalar_diff = diff
                    max_scalar_key = key
                    max_scalar_name = f"stress.{skey}"

        current_thermo = current_entry.get("thermo")
        generated_thermo = generated_entry.get("thermo")
        if (current_thermo is None) != (generated_thermo is None):
            return False, f"thermo presence differs for {key}"
        if current_thermo is not None:
            if set(current_thermo) != set(generated_thermo):
                return False, f"thermo keys differ for {key}"
            for tkey in sorted(current_thermo):
                diff = abs(
                    float(current_thermo[tkey]) - float(generated_thermo[tkey])
                )
                if diff > max_scalar_diff:
                    max_scalar_diff = diff
                    max_scalar_key = key
                    max_scalar_name = f"thermo.{tkey}"

        current_force_file = current_entry.get("forces_file")
        generated_force_file = generated_entry.get("forces_file")
        if (current_force_file is None) != (generated_force_file is None):
            return False, f"forces_file presence differs for {key}"
        if current_force_file is not None:
            current_force = np.asarray(
                np.load(current_root / current_force_file), dtype=np.float64
            )
            generated_force = np.asarray(
                np.load(generated_root / generated_force_file), dtype=np.float64
            )
            if current_force.shape != generated_force.shape:
                return (
                    False,
                    f"Force shape differs for {key}: current={current_force.shape}, "
                    f"generated={generated_force.shape}",
                )
            force_diff = float(np.max(np.abs(current_force - generated_force)))
            if force_diff > max_force_diff:
                max_force_diff = force_diff
                max_force_key = key

        current_charges_file = current_entry.get("charges_file")
        generated_charges_file = generated_entry.get("charges_file")
        if (current_charges_file is None) != (generated_charges_file is None):
            return False, f"charges_file presence differs for {key}"
        if current_charges_file is not None:
            current_charges = np.asarray(
                np.load(current_root / current_charges_file), dtype=np.float64
            )
            generated_charges = np.asarray(
                np.load(generated_root / generated_charges_file),
                dtype=np.float64,
            )
            if current_charges.shape != generated_charges.shape:
                return (
                    False,
                    f"Charge shape differs for {key}: current={current_charges.shape}, "
                    f"generated={generated_charges.shape}",
                )
            charge_diff = float(
                np.max(np.abs(current_charges - generated_charges))
            )
            if charge_diff > max_charge_diff:
                max_charge_diff = charge_diff
                max_charge_key = key

    if max_scalar_diff > abs_tol_scalar:
        return (
            False,
            f"Scalar differs above tolerance: max_diff={max_scalar_diff:.3e} "
            f"at {max_scalar_key}, field={max_scalar_name}",
        )
    if max_force_diff > abs_tol_force:
        return (
            False,
            f"Force differs above tolerance: max_diff={max_force_diff:.3e} at {max_force_key}",
        )
    if max_charge_diff > abs_tol_charge:
        return (
            False,
            f"Charge differs above tolerance: max_diff={max_charge_diff:.3e} at {max_charge_key}",
        )

    return (
        True,
        f"max_scalar_diff={max_scalar_diff:.3e}, "
        f"max_force_diff={max_force_diff:.3e}, "
        f"max_charge_diff={max_charge_diff:.3e}",
    )


def main():
    repo_root = get_repo_root()
    default_output = (
        repo_root
        / "benchmarks"
        / "comparison"
        / "reference"
        / "lammps"
        / "reference.json"
    )
    default_statics = (
        repo_root / "benchmarks" / "comparison" / "tests" / "lammps" / "statics"
    )

    parser = argparse.ArgumentParser(
        description="Generate static LAMMPS reference data for comp-lammps tests."
    )
    parser.add_argument("--output", type=Path, default=default_output)
    parser.add_argument("--statics-path", type=Path, default=default_statics)
    parser.add_argument("--check", action="store_true")
    parser.add_argument("--abs-tol-scalar", type=float, default=1.0e-6)
    parser.add_argument("--abs-tol-force", type=float, default=1.0e-6)
    parser.add_argument("--abs-tol-charge", type=float, default=1.0e-6)
    args = parser.parse_args()

    if args.check:
        if not args.output.exists():
            print(f"[FAIL] Missing reference file: {args.output}")
            raise SystemExit(1)
        with tempfile.TemporaryDirectory(
            prefix="lammps_refgen_check_"
        ) as tmpdir:
            tmp_output = Path(tmpdir) / "reference.json"
            tmp_root = tmp_output.parent
            generated_payload = generate_payload(args.statics_path, tmp_root)
            tmp_output.write_text(
                json.dumps(generated_payload, indent=2) + "\n"
            )

            current_payload = json.loads(args.output.read_text())
            ok, detail = compare_payloads(
                current_payload=current_payload,
                generated_payload=generated_payload,
                current_root=args.output.parent,
                generated_root=tmp_root,
                abs_tol_scalar=args.abs_tol_scalar,
                abs_tol_force=args.abs_tol_force,
                abs_tol_charge=args.abs_tol_charge,
            )
            if ok:
                print(f"[PASS] LAMMPS reference is up to date ({detail}).")
                return
            print(f"[FAIL] LAMMPS reference differs. {detail}")
            raise SystemExit(1)

    payload = generate_payload(args.statics_path, args.output.parent)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(json.dumps(payload, indent=2) + "\n")
    print(f"[OK] Wrote {len(payload['entries'])} entries to {args.output}")


if __name__ == "__main__":
    main()

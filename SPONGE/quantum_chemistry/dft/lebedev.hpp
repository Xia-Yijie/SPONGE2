static inline bool Load_Lebedev_Angular_Grid(int n_ang,
                                             std::vector<float>& dirs,
                                             std::vector<float>& w_ang)
{
    const LebedevPoint* pts = nullptr;
    int n = 0;
    if (n_ang == 194)
    {
        pts = LEBEDEV_194;
        n = 194;
    }
    else if (n_ang == 302)
    {
        pts = LEBEDEV_302;
        n = 302;
    }
    else if (n_ang == 590)
    {
        pts = LEBEDEV_590;
        n = 590;
    }
    else
    {
        return false;
    }
    const float four_pi = 12.56637061435917295385f;
    dirs.assign(n * 3, 0.0f);
    w_ang.assign(n, 0.0f);
    for (int i = 0; i < n; i++)
    {
        dirs[i * 3 + 0] = pts[i].x;
        dirs[i * 3 + 1] = pts[i].y;
        dirs[i * 3 + 2] = pts[i].z;
        w_ang[i] = four_pi * pts[i].w;
    }
    return true;
}

